window.currentLang = localStorage.getItem('lang') || 'es';

// 2. Translation logic
window.updateLanguage = function(lang) {
  if (!window.translations[lang]) lang = 'es';
  window.currentLang = lang;
  localStorage.setItem('lang', lang);
  document.documentElement.lang = lang;

  // Update selector UI
  var langBtn = document.getElementById('lang-btn');
  var langDropdown = document.getElementById('lang-dropdown');
  if (langBtn && langDropdown) {
    var selectedOption = langDropdown.querySelector('[data-lang="' + lang + '"]');
    if (selectedOption) {
      var flagEl = selectedOption.querySelector('.lang-flag');
      var btnFlagEl = langBtn.querySelector('.lang-flag');
      if (flagEl && btnFlagEl) {
        btnFlagEl.textContent = flagEl.textContent;
      }
      var options = langDropdown.querySelectorAll('li');
      for (var i = 0; i < options.length; i++) {
        options[i].classList.remove('is-selected');
      }
      selectedOption.classList.add('is-selected');
    }
  }

  // Update text
  var i18nEls = document.querySelectorAll('[data-i18n]');
  for (var j = 0; j < i18nEls.length; j++) {
    var key = i18nEls[j].getAttribute('data-i18n');
    if (window.translations[lang][key]) {
      i18nEls[j].textContent = window.translations[lang][key];
    }
  }

  // Update placeholders
  var i18nPls = document.querySelectorAll('[data-i18n-placeholder]');
  for (var k = 0; k < i18nPls.length; k++) {
    var pKey = i18nPls[k].getAttribute('data-i18n-placeholder');
    if (window.translations[lang][pKey]) {
      i18nPls[k].placeholder = window.translations[lang][pKey];
    }
  }

  // Dispatch event (compatible)
  var event;
  if (typeof(Event) === 'function') {
    event = new Event('languageChanged');
  } else {
    event = document.createEvent('Event');
    event.initEvent('languageChanged', true, true);
  }
  window.dispatchEvent(event);
};

// 3. Initialization
(function() {
  function init() {
    var langBtn = document.getElementById('lang-btn');
    var langDropdown = document.getElementById('lang-dropdown');
    if (langBtn && langDropdown) {
      langBtn.addEventListener('click', function(e) {
        e.stopPropagation();
        langDropdown.classList.toggle('active');
      });
      var options = langDropdown.querySelectorAll('li');
      for (var i = 0; i < options.length; i++) {
        (function(opt) {
          opt.addEventListener('click', function() {
            window.updateLanguage(opt.getAttribute('data-lang'));
            langDropdown.classList.remove('active');
          });
        })(options[i]);
      }
      document.addEventListener('click', function() {
        langDropdown.classList.remove('active');
      });
    }
    // Perform initial translation once DOM is ready
    window.updateLanguage(window.currentLang);
  }

  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', init);
  } else {
    init();
  }
})();
